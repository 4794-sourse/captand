#include <iostream>
using namespace std;
int main() {
	//1.整型
	int a = 0;
	cout << "请给整型变量a赋值：" << endl;
	cin >> a;
	cout << "整型变量a的值为：" << a  << endl;
	//2.实型
	float b = 3.14f;
	cout << "请给实型b赋值：" << endl;
	cin >> b;
	cout << "实型变量b的值为：" << b << endl;
	//3.字符型
	char c;
	cout << "请给字符c赋值：" << endl;
	cin >> c;
	cout << "字符变量c的值为：" << c << endl;
	//4.字符串型
	//5.布尔类型
	system("pause");
	return 0;
}